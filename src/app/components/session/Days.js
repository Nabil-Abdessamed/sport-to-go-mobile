export default class Days {
  constructor({
    monday = 0,
    tuesday = 0,
    wednesday = 0,
    thursday = 0,
    friday = 0,
    saturday = 0,
    sunday = 0,
  }) {
    this.monday = monday;
    this.tuesday = tuesday;
    this.wednesday = wednesday;
    this.thursday = thursday;
    this.friday = friday;
    this.saturday = saturday;
    this.sunday = sunday;
  }
}
